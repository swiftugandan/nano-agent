/// Truncate a string at a UTF-8 char boundary, avoiding panics on multi-byte sequences.
pub fn truncate_at_boundary(s: &str, max: usize) -> &str {
    if s.len() <= max {
        s
    } else {
        let mut end = max;
        while end > 0 && !s.is_char_boundary(end) {
            end -= 1;
        }
        &s[..end]
    }
}
